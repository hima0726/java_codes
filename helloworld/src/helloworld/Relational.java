package helloworld;

public class Relational {
    public static void main(String[] args) {
		int a=5,b=20;
		boolean z;
		z=a==b;
		System.out.println("a==b"+z);
		z=a!=b;
		System.out.println("a!=b"+z);
		z=a>b;
		System.out.println("a>b"+z);
		z=a<b;
		System.out.println("a<b"+z);
		z=a<=b;
		System.out.println("a<=b"+z);
		z=a>=b;
		System.out.println("a>=b"+z);
    }
}
